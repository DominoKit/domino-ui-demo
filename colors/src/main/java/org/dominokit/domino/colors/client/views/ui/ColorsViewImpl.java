    private DivElement makeColorBox(Color color) {
        return div().addCss(dui_m_y_4, dui_flex, dui_flex_col, dui_w_full, dui_rounded_sm, dui_border, dui_border_solid, color.getBorderColor(), dui_elevation_4)
                .appendChild(div()
                        .addCss(dui_flex, dui_flex_col)
                        .appendChild(div().addCss(dui_p_4, dui_w_full, dui_text_center, dui_fg, color.getCss()).textContent(color.getCss().getCssClass()))
                        .appendChild(div().addCss(dui_p_4, dui_w_full, dui_text_center, color.getBackground()).textContent("dui_bg_" + color.getName().toLowerCase()))
                        .appendChild(div().addCss(dui_p_4, dui_w_full, dui_text_center, dui_fg_white, color.getBackground()).textContent("dui_bg_" + color.getName().toLowerCase()))
                        .appendChild(div().addCss(dui_p_4, dui_w_full, dui_text_center, color.getForeground()).textContent("dui_fg_" + color.getName().toLowerCase()))
                );
    }

