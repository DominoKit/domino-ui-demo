        element.appendChild(LinkToSourceCode.createLink("menu", MenuViewImpl.class));
        element.appendChild(BlockHeader.create("Menu"));

        basicMenu();
        element.appendChild(CodeCard.createLazyCodeCard(CodeResource.INSTANCE.basicMenu()));

        basicMenuWithHeaderAndActions();
        element.appendChild(CodeCard.createLazyCodeCard(CodeResource.INSTANCE.basicMenuWithHeaderAndActions()));

        basicMenuSearchableAndAddMissing();
