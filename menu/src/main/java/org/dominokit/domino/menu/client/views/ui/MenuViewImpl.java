        element.appendChild(CodeCard.createLazyCodeCard(CodeResource.INSTANCE.basicMenuAllBasicFeatures()));

        embedIntoElements();
        element.appendChild(CodeCard.createLazyCodeCard(CodeResource.INSTANCE.embedIntoElements()));

        dropMenu();
        element.appendChild(CodeCard.createLazyCodeCard(CodeResource.INSTANCE.dropMenu()));

